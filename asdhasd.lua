function Type(text)
    count = string.len(text)
    for i = 1, count, 1 do
    game.Players.LocalPlayer.PlayerGui.Home.hud.Topbar.titleBar.Title.Text = string.sub(text, 1, i)
    wait(0.05)
    end
    wait(2)
    for i = count, 0, -1 do
    game.Players.LocalPlayer.PlayerGui.Home.hud.Topbar.titleBar.Title.Text = string.sub(text, 1, i)
    wait(0.05)
    end
    return Type()
end

